async function main() {
  const { db, exercises, foods } = await import('../src/lib/db/index');
  const { sql } = await import('drizzle-orm');

  console.log('Truncating tables...');
  await db.execute(sql`TRUNCATE TABLE template_exercises, workout_templates, workout_sets, workouts, meal_logs, food_favorites, foods, exercises RESTART IDENTITY CASCADE;`);

  console.log(`Seeding ${exerciseRows.length} exercises...`);
  await db.insert(exercises).values(exerciseRows);

  console.log(`Seeding ${foodRows.length} foods...`);
  // Drizzle has insert size limits, batch in 50s
  for (let i = 0; i < foodRows.length; i += 50) {
    await db.insert(foods).values(foodRows.slice(i, i + 50));
  }

  console.log('Done.');
  process.exit(0);
}

main().catch((e) => {
  console.error(e);
  process.exit(1);
});
